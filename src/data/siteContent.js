export const getServiceIcon = (title = '') => {
  if (/solar/i.test(title)) return 'solar';
  if (/electrical|cabling|wiring|lighting/i.test(title)) return 'power';
  if (/paint/i.test(title)) return 'paint';
  if (/plumb/i.test(title)) return 'pipe';
  return 'build';
};

export const normalizeService = (service, fallbackIndex = 0) => {
  const fallback = fallbackServices[fallbackIndex % fallbackServices.length];
  return {
    ...fallback,
    ...service,
    id: String(service?.id ?? fallback.id),
    title: service?.title || fallback.title,
    description: service?.description || fallback.description,
    longDescription: service?.longDescription || service?.description || fallback.longDescription,
    icon: service?.icon || getServiceIcon(service?.title || fallback.title),
    image: service?.image || fallback.image,
    highlights: service?.highlights || fallback.highlights
  };
};

export const normalizeProject = (project, fallbackIndex = 0) => {
  const fallback = fallbackProjects[fallbackIndex % fallbackProjects.length];
  return {
    ...fallback,
    ...project,
    id: String(project?.id ?? fallback.id),
    title: project?.title || fallback.title,
    location: project?.location || fallback.location,
    description: project?.description || fallback.description,
    longDescription: project?.longDescription || project?.description || fallback.longDescription,
    image: project?.images && project.images.length ? project.images[0] : project?.image || fallback.image,
    category: project?.category || project?.serviceTitle || fallback.category,
    serviceId: String(project?.serviceId ?? ''),
    serviceTitle: project?.serviceTitle || project?.category || fallback.category
  };
};

export const getMergedProducts = (projects = []) => {
  const normalizedRemote = Array.isArray(projects) ? projects.map(normalizeProject) : [];
  const catalog = serviceCatalogProducts.map(normalizeProject);
  const merged = new Map();

  [...catalog, ...normalizedRemote].forEach((item) => {
    merged.set(item.id, item);
  });

  return Array.from(merged.values());
};

